# Fitting Decision Tree Regression to the dataset
# if we didnt add the parameter control to regressor,
# y_pred will be 250000 which is the mean value of all values of DV
# we know that decision trees returns the mean value if we had no splits
# to avoid that, we should specify the split value
# that way, depending on our IV, it gets narrowed down
# to some split block and that blocks mean value will be the value of y_pred
# which is a far better prediction than the one without splits


install.packages('rpart')
library(rpart)
regressor = rpart(formula = Salary ~ .,
                  data = dataset,
                  control = rpart.control(minsplit = 1))

# Predicting a new result with Decision Tree Regression
y_pred = predict(regressor, data.frame(Level = 6.5))

# Visualising the Decision Tree Regression results (higher resolution)
# install.packages('ggplot2')
library(ggplot2)
x_grid = seq(min(dataset$Level), max(dataset$Level), 0.01)
ggplot() +
  geom_point(aes(x = dataset$Level, y = dataset$Salary),
             colour = 'red') +
  geom_line(aes(x = x_grid, y = predict(regressor, newdata = data.frame(Level = x_grid))),
            colour = 'blue') +
  ggtitle('Truth or Bluff (Decision Tree Regression)') +
  xlab('Level') +
  ylab('Salary')

# Plotting the tree
plot(regressor)
